#3.Assignment Operators
x=10
x+=10
print('Add & Assign(x+=5):',x)#Add & Assign(x+=5): 20
x-=10
print('Subtract & Assign(x-=5):',x)#Subtract & Assign(x-=5): 10 
x*=10
print('Multiply & Assign(x*=2):',x)#Multiply & Assign(x*=2): 100 
x/=10
print('Divide & Assign(x/=2):',x)#Divide & Assign(x/=2): 10.0  
x//=10
print('Floor divide & Assign(x//=5):',x)#Floor divide & Assign(x//=5): 1.0 
x%=10
print('Modulus & Assign(x%=5):',x)#Modulus & Assign(x%=5): 1.0   
x**=10
print('Exponentiation & Assign(x**=3):',x)#Exponentiation & Assign(x**=3): 1.0